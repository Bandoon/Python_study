print(11415+11111)        #print在python里面有输出的功能  这里的数字就是数字，具有加减乘除功能。
print("114514+11111")  #当在print括号内加上英文的引号后，输出的就是字符串，数字不在具有数学运算的功能。

#当一句话当中有多个引号时
print('He said"how are you"')  #可以用单引号和双引号区分开，但是只适用于输出的内容中只有一种引号
print("He said\"let\'go\'\"")   #可以在输出的句子中的引号前加上\，这样表示这只是一个单纯的引号。

#当想要输出的内容中要换行
print("""朝辞白帝彩云间,
千里江陵一日还.""")              #可以使用三引号，然后在需要换行的地方直接回车。
print("朝辞白帝彩云间，\n千里江陵一日还.")  #也可以在需要换行的地方插入一个"\n"，效果是一样的。
